class OrderListCreateView(APIView):
    def get(self, request):
        customer_name = request.query_params.get('customer')
        product_names = request.query_params.get('products')

        orders = Order.objects.all()

        if customer_name:
            orders = orders.filter(customer__name__icontains=customer_name)

        if product_names:
            product_names_list = product_names.split(',')
            orders = orders.filter(orderitem__product__name__in=product_names_list).distinct()

        serializer = OrderSerializer(orders, many=True)
        return Response(serializer.data)

    def post(self, request):
        serializer = OrderSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)

